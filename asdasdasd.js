function getUniqueKNumbers(arr) {
    const seen = new Set();
    const uniqueKNumbers = [];

    arr.forEach(num => {
        const baseNum = num.slice(0, -1); // Remove the last character
        if (num.endsWith('K') && !seen.has(baseNum)) {
            seen.add(baseNum);
            uniqueKNumbers.push(num);
        }
    });

    return uniqueKNumbers;
}

